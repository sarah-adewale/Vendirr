const mongoose = require("mongoose");

const PostSchema = new mongoose.Schema({
  vendorName: {
    type: String,
    required: true,
  },
  tags:{
    type: String,
    required: true,
  },
  image: {
    type: String,
    require: true,
  },
  cloudinaryId: {
    type: String,
    require: true,
  },
  caption: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  reviews: {
    type: Number,
    required: true,
  },
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("Post", PostSchema);
